class WF(nn.Module):
    def __init__(self, in_channels=128, decode_channels=128, eps=1e-8):
        super(WF, self).__init__()
        self.pre_conv = Conv(in_channels, decode_channels, kernel_size=1)

        self.weights = nn.Parameter(torch.ones(2, dtype=torch.float32), requires_grad=True)
        self.eps = eps
        self.post_conv = ConvBNReLU(decode_channels, decode_channels, kernel_size=3)

    def forward(self, x, res):

        weights = nn.ReLU()(self.weights)
        fuse_weights = weights / (torch.sum(weights, dim=0) + self.eps)

        x = fuse_weights[0] * self.pre_conv(res) + fuse_weights[1] * x
        x = self.post_conv(x)
        return x

class WS(nn.Module):
    def __init__(self, in_channels=128, decode_channels=128, eps=1e-8):
        super(WS, self).__init__()
        self.pre_conv = Conv(in_channels, in_channels, kernel_size=1)
        self.pre_conv2 = Conv(in_channels, in_channels, kernel_size=1)
        self.weights = nn.Parameter(torch.ones(3, dtype=torch.float32), requires_grad=True)
        self.eps = eps
        self.post_conv = ConvBNReLU(in_channels, decode_channels, kernel_size=3)

    def forward(self, x, res,ade):
        weights = nn.ReLU()(self.weights)
        fuse_weights = weights / (torch.sum(weights, dim=0) + self.eps)
        x = fuse_weights[0] * self.pre_conv(res) + fuse_weights[1] * x + fuse_weights[2]*ade
        x = self.post_conv(x)
        return x



class AuxHead(nn.Module):

    def __init__(self, in_channels=64, num_classes=8):
        super().__init__()
        self.conv = ConvBNReLU(in_channels, in_channels)
        self.drop = nn.Dropout(0.1)
        self.conv_out = Conv(in_channels, num_classes, kernel_size=1)

    def forward(self, x, h, w):
        feat = self.conv(x)
        feat = self.drop(feat)
        feat = self.conv_out(feat)
        feat = F.interpolate(feat, size=(h, w), mode='bilinear', align_corners=False)
        return feat

class VAN(nn.Module):
    def __init__(self, img_size=224, in_chans=3, num_classes=1000,embed_dims=[64, 128, 256, 512],
                    mlp_ratios=[4, 4, 4, 4], drop_rate=0., drop_path_rate=0., norm_layer=nn.LayerNorm,
                    whetherKan=False,
                    depths=[3, 4, 6, 3], num_stages=4, pretrained_cfg=None, flag=False,window_size=8):                    # !!! 后加 pretrained_cfg=None,
        super().__init__()
        if flag == False:
            self.num_classes = num_classes
        self.depths = depths
        self.num_stages = num_stages
        self.conv1x1 = nn.Conv2d(273, 64, kernel_size=1, stride=1, padding=0)
        self.CFFM_L = CFFM(16, num_heads=8, LayerNorm_type='WithBias')
        self.CFFM_H = CFFM(16, num_heads=8, LayerNorm_type='WithBias')
        self.fuseFeature = FMS(in_ch=64, out_ch=16, num_heads=8, window_size=window_size)
        self.WF1 = WF(in_channels=16, decode_channels=16)
        self.WF2 = WF(in_channels=32, decode_channels=16)


        self.down = Conv(in_channels=64, out_channels=16, kernel_size=1)
        self.head=None

        dpr = [x.item() for x in torch.linspace(0, drop_path_rate, sum(depths))]  # stochastic depth decay rule
        cur = 0

        for i in range(num_stages):
            patch_embed = OverlapPatchEmbed(img_size=img_size if i == 0 else img_size // (2 ** (i + 1)),
                                            patch_size=7 if i == 0 else 3,
                                            stride=4 if i == 0 else 2,
                                            in_chans=in_chans if i == 0 else embed_dims[i - 1],
                                            embed_dim=embed_dims[i])

            block = nn.ModuleList([Block(
                dim=embed_dims[i], mlp_ratio=mlp_ratios[i], drop=drop_rate, drop_path=dpr[cur + j])
                for j in range(depths[i])])
            norm = norm_layer(embed_dims[i])
            cur += depths[i]

            setattr(self, f"patch_embed{i + 1}", patch_embed)# 设置 patch_embed 层
            setattr(self, f"block{i + 1}", block)
            setattr(self, f"norm{i + 1}", norm)

        if num_classes > 0:
            final_channels = 16
            self.head = nn.Linear(final_channels, num_classes)
        else:
            self.head = nn.Identity()

        self.apply(self._init_weights)

    def _init_weights(self, m):
        if isinstance(m, nn.Linear):
            trunc_normal_(m.weight, std=.02)
            if isinstance(m, nn.Linear) and m.bias is not None:
                nn.init.constant_(m.bias, 0)
        elif isinstance(m, nn.LayerNorm):
            nn.init.constant_(m.bias, 0)
            nn.init.constant_(m.weight, 1.0)
        elif isinstance(m, nn.Conv2d):
            fan_out = m.kernel_size[0] * m.kernel_size[1] * m.out_channels
            fan_out //= m.groups
            m.weight.data.normal_(0, math.sqrt(2.0 / fan_out))
            if m.bias is not None:
                m.bias.data.zero_()

    def freeze_patch_emb(self):
        self.patch_embed1.requires_grad = False

    @torch.jit.ignore
    def no_weight_decay(self):
        return {'pos_embed1', 'pos_embed2', 'pos_embed3', 'pos_embed4', 'cls_token'}  # has pos_embed may be better

    def get_classifier(self):
        return self.head

    def reset_classifier(self, num_classes, global_pool=''):
        self.num_classes = num_classes
        self.head = nn.Linear(64, num_classes) if num_classes > 0 else nn.Identity()

    def forward_features(self, x):
        B = x.shape[0]
        stage_features = []
        sta1 = None

        for i in range(self.num_stages):
            patch_embed = getattr(self, f"patch_embed{i + 1}")
            block = getattr(self, f"block{i + 1}")
            norm = getattr(self, f"norm{i + 1}")

            x, H, W = patch_embed(x)

            if i == 0:
                sta1 = x
                sta1h, sta1w = H, W

            for blk in block:
                x = blk(x)

            x = x.flatten(2).transpose(1, 2)
            x = norm(x)

            if i != self.num_stages - 1:
                x = x.reshape(B, H, W, -1).permute(0, 3, 1, 2).contiguous()

            if i >= 1:
                stage_features.append(x)

        base_size = stage_features[0].shape[-2:]

        for idx, feature in enumerate(stage_features):
            if feature.dim() == 3:
                N, C, L = feature.shape
                feature = feature.view(N, C, int(L ** 0.5), int(L ** 0.5))
            elif feature.dim() != 4:
                raise ValueError(f"Unexpected feature dimension: {feature.shape}")
            stage_features[idx] = F.interpolate(feature, size=base_size, mode='bicubic', align_corners=False)

        concatenated_features = torch.cat(stage_features, dim=1)
        concatenated_features = self.conv1x1(concatenated_features)
        concatenated_features = F.interpolate(
            concatenated_features, size=(32, 32), mode='bilinear', align_corners=False
        )
        return concatenated_features,sta1,sta1h, sta1w


    def forward(self, x,imagename=None):

        x, sta1,sta1h, sta1w = self.forward_features(x)

        fusefeature_L,fusefeature_H,glb,local = self.fuseFeature(x,imagename)
        glb = self.CFFM_L(fusefeature_L,glb)
        local = self.CFFM_H(fusefeature_H,local)


        res  = self.WF1(glb,local)
        #print(res.shape)
        middleres = self.down(x)

        res = F.interpolate(res,size=(sta1h,sta1w), mode='bicubic', align_corners=False)
        middleres = F.interpolate(middleres, size=res.shape[2:], mode='bilinear', align_corners=False)
        #print(middleres.shape)
        #print(res.shape)
        res = middleres + res

        res = self.WF2(res,sta1)

        if self.head is None:
            num_features = x.size(1)
            self.head = nn.Linear(num_features, self.num_classes).to(x.device)
        x = F.adaptive_avg_pool2d(res, output_size=(1, 1))
        x = x.view(x.size(0), -1)
        x = self.head(x)

        return x

class DWConv(nn.Module):
    def __init__(self, dim=768):
        super(DWConv, self).__init__()
        self.dwconv = nn.Conv2d(dim, dim, 3, 1, 1, bias=True, groups=dim)

    def forward(self, x):
        x = self.dwconv(x)
        return x


def _conv_filter(state_dict, patch_size=16):
    """ convert patch embedding weight from manual patchify + linear proj to conv"""
    out_dict = {}
    for k, v in state_dict.items():
        if 'patch_embed.proj.weight' in k:
            v = v.reshape((v.shape[0], 3, patch_size, patch_size))
        out_dict[k] = v

    return out_dict


